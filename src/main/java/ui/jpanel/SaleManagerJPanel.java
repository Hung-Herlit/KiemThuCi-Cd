    @Override
    public void edit() {
        int selectedRow = tblBills.getSelectedRow(); // lấy index của dòng được chọn
        if (selectedRow != -1) { // -1 nghĩa là chưa chọn dòng nào
            Object value = tblBills.getValueAt(selectedRow, 0); // cột đầu tiên => index = 0
            SaleManagerJPanel.this.showBillJDialog((int) value);
            this.fillToTable();
        }

    }

