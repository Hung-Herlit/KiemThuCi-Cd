    public void showBillJDialog(int billId) { // Hiển thị cửa số phiếu bán hàng của thẻ
        BillDAO dao = new BillDAOImpl();
        Bill bill = dao.findServicingByCardId(billId); // tải bill đang phục vụ của thẻ
        this.fillToTable();
        Frame owner = (Frame) SwingUtilities.getWindowAncestor(this);
        BillJDialog dialog = new BillJDialog(owner, true);

        dialog.setBill(bill); // Cần khai báo vào BillJDialog @Setter Bill bill
        dialog.setVisible(true);
    }

